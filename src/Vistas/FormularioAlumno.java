    private void jfGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jfGuardarActionPerformed
 Alumno al= new Alumno();
 
        if (jfNombre.getText().length()==0){
             JOptionPane.showMessageDialog(this,"Debe ingresar un Nombre ");
            jfNombre.requestFocus();   
  } else{
            if(0==jfApellido.getText().length()){
             JOptionPane.showMessageDialog(this,"Debe ingresar un Apellido ");
            jfApellido.requestFocus();   
  }else{  try {      
       al.setLegajo(Integer.parseInt(jfLegajo.getText()));
       al.setNombre(jfNombre.getText());
       al.setApellido(jfApellido.getText());
        alum.guardarAlumno(al);
      
      }
      catch(Exception e){
              
              JOptionPane.showMessageDialog(this,"ERROR - No ha llenado correctamente los campos");
              
              }
      
      limpiarCampos();
   
          }
    }//GEN-LAST:event_jfGuardarActionPerformed
    }
      public void limpiarCampos(){
    jfApellido.setText("");
    jfLegajo.setText("");
    jfNombre.setText("");
    desactivarCampos();
    jfGuardar.setEnabled(false);
}

    private void jfLegajoFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jfLegajoFocusLost
           String value="[0-9]*";
      
        if (!jfLegajo.getText().matches(value)){
          
            JOptionPane.showMessageDialog(this,"ERROR - Solo n√∫meros");
            jfLegajo.requestFocus();
    }                                  
    
    }//GEN-LAST:event_jfLegajoFocusLost
    
   
           private void activarCampos() {
        
        jfLegajo.setEnabled(true);
        jfApellido.setEnabled(true);
        jfNombre.setEnabled(true);
    
    }
    
     
    private void desactivarCampos() {
    
        jfLegajo.  setEnabled(false);
        jfApellido.setEnabled(false);
        jfNombre.setEnabled(false);
        
        
    } 
    

